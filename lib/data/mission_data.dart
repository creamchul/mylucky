  /// 날짜 기반 오늘의 미션 가져오기 (같은 날은 같은 미션)
  static String getTodayMission(DateTime date) {
    final dateString = '${date.year}${date.month.toString().padLeft(2, '0')}${date.day.toString().padLeft(2, '0')}';
    final seed = dateString.hashCode;
    final index = seed % missions.length;
    return missions[index.abs()];
  }

  /// 랜덤 미션 가져오기
  static String getRandomMission() {
