  /// 랜덤 메시지 가져오기
  static String getRandomMessage() {
    final random = Random();
    return messages[random.nextInt(messages.length)];
  }

  /// 날짜 기반 오늘의 메시지 가져오기 (같은 날은 같은 메시지)
  static String getTodayMessage(DateTime date) {
    final dateString = '${date.year}${date.month.toString().padLeft(2, '0')}${date.day.toString().padLeft(2, '0')}';
    final seed = dateString.hashCode;
    final index = seed % messages.length;
    return messages[index.abs()];
  }

  /// 특정 인덱스의 메시지 가져오기
  static String getMessageByIndex(int index) {
    if (index >= 0 && index < messages.length) {
      return messages[index];
    }
    return getRandomMessage();
  }

  /// 전체 메시지 개수
  static int get totalCount => messages.length;
